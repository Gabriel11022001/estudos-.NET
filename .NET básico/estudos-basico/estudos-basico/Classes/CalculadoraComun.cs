using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace estudos_basico.Classes
{
    internal class CalculadoraComun
    {

        /**
         * recebe dois inteiros como argumento e retorna um inteiro
         */
        public int Somar(int primeiroValor, int segundoValor)
        {

            return primeiroValor + segundoValor;
        }

        /**
         * recebe dois inteiros como argumento e retorna um inteiro
         */
        public int Subtrair(int primeiroValor, int segundoValor)
        {

            return primeiroValor - segundoValor;
        }

    }
}
